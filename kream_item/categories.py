def normalize(text: str) -> str:
    return re.sub(r"[\s·/_(),.\-]+", "", text or "").casefold()


def find_category(value: str) -> Category:
    wanted = normalize(value)
    matches = [c for c in CATEGORIES if wanted in {normalize(c.path), normalize(c.leaf)}]
    if len(matches) != 1:
        choices = ", ".join(c.path for c in matches[:5]) or "없음"
        raise ValueError(f"카테고리를 하나로 찾을 수 없습니다: {value!r} (후보: {choices})")
    return matches[0]


def classify(text: str, seed: Category | None = None) -> Category:
    haystack = normalize(text)
    best = seed
    best_score = 2 if seed else -1
    for category in CATEGORIES:
        terms = (category.leaf, *category.aliases, *ALIASES.get(category.leaf, ()))
        score = max((len(normalize(term)) for term in terms if normalize(term) in haystack), default=0)
        if seed and category == seed:
            score += 3
        if score > best_score:
            best, best_score = category, score
    if best is None:
        raise ValueError("분류할 수 없는 상품입니다. 검색 카테고리를 지정하세요.")
    return best

